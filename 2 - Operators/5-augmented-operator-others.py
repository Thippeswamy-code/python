# Describes basic usage of all the augmented operators available

spam = 40

# Addition
spam += 1
print(spam)

# Subtraction
spam -= 1
print(spam)

# Multiplication
spam *= 1
print(spam)

# Division
spam /= 2
print(spam)

# Modulus
spam %= 3
print(spam)

# Example Usage
strOne = 'Testing'
listOne = [1, 2]

strOne += ' String'  # concat for strings and lists
print(strOne)
listOne *= 2  # replication for strings and lists
print(listOne)
