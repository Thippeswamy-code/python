# Describes basic usage of augmented operators

spam = 42
spam = spam + 1 # Line 4 gives similar behaviour as line 6
print(spam)
spam += 1 # Line 4 gives similar behaviour as line 6
print(spam)

spam = 42
spam = 1
spam = spam - 1 # Line 13 gives similar behaviour as line 11
print(spam)
spam -= 1 # Line 13 gives similar behaviour as line 11
print(spam)
