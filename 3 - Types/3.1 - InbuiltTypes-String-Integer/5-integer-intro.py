# Describes what is a integer and it details
# int() is a function that converts a integer like characters to a integer
# float() is a function that converts a integer or float like characters to a float

# Working wih integer
integer1 = 1
integer2 = int("2")
integer3 = int("3")
integer4 = int(4)

print(integer1)
print(integer2)
print(integer3)
print(integer4)

# Working wih float
float1 = 1.234
float2 = float("5")
float3 = float("6.789")
float4 = float(1)

print(float1)
print(float2)
print(float3)
print(float4)
