# Describes integer manipulation, methods available, and it details

# Main methods of integer type
integer_val = int(1.2345)
print('integer_val.bit_length()', integer_val.bit_length())
print('integer_val.conjugate()', integer_val.conjugate())
print('integer_val.denominator', integer_val.denominator)
print('integer_val.denominator', integer_val.bit_length())

# Main methods of float type
float_val = float(1.2345)
print('float_val.conjugate()', float_val.conjugate())
print('float_val.is_integer()', float_val.is_integer())
